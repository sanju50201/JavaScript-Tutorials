console.log("Arithmetic Operators in JS")
let a = 10
let b = 4
console.log("a + b: ", a + b) // addition operator
console.log("a - b: ", a - b) // subtraction operator
console.log("a * b: ", a * b) // multiplication operator
console.log("a / b: ", a / b) // division 
console.log("a ** b: ", a ** b) //exponential
console.log("a % b: ", a % b) //modulus
console.log("++a: ", ++a) // ++a increments the a value then prints it
console.log("a++: ", a++) // a++ prints the a value then increments it
console.log("--a: ", --a) // --a decrements then prints
console.log("a--:", a--) // a-- prints then decrements
console.log(a)
